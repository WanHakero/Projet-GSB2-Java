package Class;

public class Statut {
	//Atribue
	private String statut;
	
	//Constructeur
	public Statut(String unStatut) {
		this.statut = unStatut;
	}

	//Getters et Setters
	public String getStatus() {
		return statut;
	}

	public void setStatus(String status) {
		this.statut = status;
	}

}
